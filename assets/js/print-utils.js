/**
 * MIAUDITOPS — Print Utility
 * Opens a styled print-friendly popup for tables and reports.
 * Usage: printReport({ title, subtitle, columns, rows, footer, orientation })
 *
 * columns: [{label:'Name', key:'name', align:'left|center|right', fmt:val=>formattedVal}]
 * rows:    array of objects matching column keys
 * footer:  optional HTML string for footer row
 */
function printReport(opts = {}) {
    const {
        title = 'Report',
        subtitle = '',
        columns = [],
        rows = [],
        footer = '',
        orientation = 'portrait',   // 'portrait' or 'landscape'
        companyName = document.title.split('—')[1]?.trim() || 'MIAUDITOPS',
    } = opts;

    const now = new Date();
    const dateStr = now.toLocaleDateString('en-NG', { day: '2-digit', month: 'short', year: 'numeric' });
    const timeStr = now.toLocaleTimeString('en-NG', { hour: '2-digit', minute: '2-digit' });

    const thead = columns.map(c =>
        `<th style="text-align:${c.align || 'left'};padding:8px 10px;font-size:11px;font-weight:700;text-transform:uppercase;color:#64748b;border-bottom:2px solid #e2e8f0;white-space:nowrap;">${c.label}</th>`
    ).join('');

    const tbody = rows.map((row, idx) => {
        const cells = columns.map(c => {
            let val = row[c.key] ?? '';
            if (c.fmt) val = c.fmt(val, row);
            return `<td style="text-align:${c.align || 'left'};padding:7px 10px;font-size:12px;border-bottom:1px solid #f1f5f9;${c.bold ? 'font-weight:700;' : ''}">${val}</td>`;
        }).join('');
        const bg = idx % 2 === 0 ? '#ffffff' : '#f8fafc';
        return `<tr style="background:${bg}">${cells}</tr>`;
    }).join('');

    const html = `<!DOCTYPE html>
<html>
<head>
    <meta charset="UTF-8">
    <title>${title} — ${companyName}</title>
    <style>
        @page { size: ${orientation}; margin: 15mm; }
        * { margin:0; padding:0; box-sizing:border-box; }
        body { font-family: 'Segoe UI', -apple-system, Arial, sans-serif; color: #1e293b; }
        .header { display:flex; justify-content:space-between; align-items:flex-start; padding-bottom:12px; border-bottom:3px solid #6d28d9; margin-bottom:16px; }
        .header-left h1 { font-size:18px; font-weight:900; color:#4c1d95; margin-bottom:2px; }
        .header-left p { font-size:11px; color:#64748b; }
        .header-right { text-align:right; font-size:10px; color:#94a3b8; line-height:1.6; }
        .header-right strong { color:#334155; font-size:11px; }
        table { width:100%; border-collapse:collapse; margin-bottom:12px; }
        .footer-row td { padding:8px 10px; font-weight:800; font-size:12px; border-top:2px solid #cbd5e1; background:#f1f5f9; }
        .print-footer { text-align:center; font-size:9px; color:#94a3b8; margin-top:20px; padding-top:8px; border-top:1px solid #e2e8f0; }
        @media print { body { -webkit-print-color-adjust: exact !important; print-color-adjust: exact !important; } }
    </style>
</head>
<body>
    <div class="header">
        <div class="header-left">
            <h1>${title}</h1>
            ${subtitle ? `<p>${subtitle}</p>` : ''}
        </div>
        <div class="header-right">
            <strong>${companyName}</strong><br>
            ${dateStr} · ${timeStr}<br>
            ${rows.length} record${rows.length !== 1 ? 's' : ''}
        </div>
    </div>
    <table>
        <thead><tr>${thead}</tr></thead>
        <tbody>${tbody}</tbody>
        ${footer ? `<tfoot><tr class="footer-row">${footer}</tr></tfoot>` : ''}
    </table>
    <div class="print-footer">Generated by MIAUDITOPS · ${dateStr}</div>
    <script>window.onload=function(){window.print();}<\/script>
</body>
</html>`;

    const win = window.open('', '_blank', 'width=900,height=700');
    win.document.write(html);
    win.document.close();
}

/** Currency formatter shorthand */
function _pFmt(v) {
    return '₦' + parseFloat(v || 0).toLocaleString('en-NG', { minimumFractionDigits: 2 });
}
